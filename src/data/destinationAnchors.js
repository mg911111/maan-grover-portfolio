export function getDestinationAnchor(sectionOrId) {
  const rawId = typeof sectionOrId === "string" ? sectionOrId : sectionOrId?.id;
  const id = rawId === "contact" ? "socials" : rawId === "about" ? "interests" : rawId;
  return PORTFOLIO_DESTINATION_ANCHORS[id] || null;
}

export function getDestinationAnchorLabel(sectionOrId) {
  const anchor = getDestinationAnchor(sectionOrId);
  if (anchor?.label) return anchor.label;
  return sectionOrId?.shortLabel || sectionOrId?.title || "Stop";
}
